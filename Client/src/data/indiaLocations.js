// Helper: get all state names
export const getStates = () => INDIA_LOCATIONS.map(s => s.state)

// Helper: get districts for a state
export const getDistricts = (stateName) => {
  const found = INDIA_LOCATIONS.find(s => s.state === stateName)
  return found ? found.districts.map(d => d.name) : []
}

// Helper: get cities for a state + district
export const getCities = (stateName, districtName) => {
  const state = INDIA_LOCATIONS.find(s => s.state === stateName)
  if (!state) return []
  const district = state.districts.find(d => d.name === districtName)
  return district ? district.cities : []
}

// Flat array for DB seeding
export const getFlatLocations = () =>
  INDIA_LOCATIONS.flatMap(s =>
    s.districts.flatMap(d =>
      d.cities.map(city => ({ state: s.state, district: d.name, city }))
    )
  )
